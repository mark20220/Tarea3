
/**
 * Clase Doble para devolver los dos posibles valores de x:(x1, x2).
 * 
 * Mark
 * 20/04/2021
 */
public class Doble
{
    double x1;
    double x2;
    public Doble(double x1, double x2){
        this.x1=x1;
        this.x2=x2;
    }
    
    double getx1(){
        return x1;
    }
    
    double getx2(){
        return x2;
    }
}
