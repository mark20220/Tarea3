
/**
 * Clase Punto para tener dos valores (x, y).
 * 
 * Mark
 * 20/04/2021
 */
public class Punto
{
    int X;
    int Y;
    public Punto(int X, int Y){
        this.X=X;
        this.Y=Y;
    }
    
    public int getX(){
        return X;
    }
    
    public int getY(){
        return Y;
    }
}
